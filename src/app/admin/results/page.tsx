import { AdminLayout } from "@/components/admin-layout";
import { ResultConfirmForm } from "@/components/champion-admin-actions";
import { CHAMPION_COUNTRIES, formatDateTime } from "@/lib/champion-guess";
import { ensureChampionSettings } from "@/lib/champion-admin";
import { createServiceClient } from "@/lib/supabase/service";

export const dynamic = "force-dynamic";

export default async function AdminResultsPage() {
  const settings = await ensureChampionSettings();
  const supabase = createServiceClient();
  const [{ count: correctGuessers }, { count: winners }] = await Promise.all([
    settings.official_champion_country
      ? supabase
          .from("players")
          .select("id", { count: "exact", head: true })
          .eq("selected_country", settings.official_champion_country)
          .eq("is_disqualified", false)
      : Promise.resolve({ count: 0 }),
    supabase.from("winners").select("id", { count: "exact", head: true }).eq("is_winner", true),
  ]);

  return (
    <AdminLayout active="/admin/results">
      <div className="mb-6">
        <p className="text-sm font-black uppercase tracking-[0.25em] text-[#128c4a]">
          Result Management
        </p>
        <h1 className="mt-2 text-4xl font-black">Champion Result</h1>
      </div>

      <div className="grid gap-5 lg:grid-cols-[minmax(0,1fr)_360px]">
        <ResultConfirmForm
          champion={settings.official_champion_country}
          resultConfirmed={settings.result_confirmed}
          countries={CHAMPION_COUNTRIES.map((country) => country.name)}
        />

        <section className="rounded-2xl bg-white p-5 shadow">
          <h2 className="text-2xl font-black">Current Status</h2>
          <div className="mt-4 grid gap-3 text-sm">
            <Info label="Official Champion" value={settings.official_champion_country ?? "Not set"} />
            <Info label="Result Confirmed" value={settings.result_confirmed ? "Yes" : "No"} />
            <Info label="Confirmed At" value={formatDateTime(settings.result_confirmed_at)} />
            <Info label="Correct Guessers" value={correctGuessers ?? 0} />
            <Info label="Winners" value={winners ?? 0} />
          </div>
          <div className="mt-4 rounded-xl bg-yellow-50 p-4 text-sm font-bold text-[#7a5200]">
            Once result is confirmed, winners are generated by correct champion pick and earliest submission
            time.
          </div>
        </section>
      </div>
    </AdminLayout>
  );
}

function Info({ label, value }: { label: string; value: React.ReactNode }) {
  return (
    <div className="flex items-center justify-between rounded-lg bg-slate-50 p-3">
      <span className="font-bold text-slate-500">{label}</span>
      <strong>{value}</strong>
    </div>
  );
}
